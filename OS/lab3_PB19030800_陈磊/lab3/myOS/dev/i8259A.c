#include "io.h"

void init8259A(void){
		outb( 0x21, 0xff);
		outb( 0xA1, 0xff);
		outb( 0x20, 0x11);
		outb( 0xA0, 0x11);
		outb( 0x21, 0x20);
		outb( 0xA1, 0x28);
		outb( 0x21, 0x04);
		outb( 0xA1, 0x02);
		outb( 0x21, 0x03);
		outb( 0xA1, 0x01);	//初始化完成
		outb( 0x21, 0x00);	//主片最低位置0,允许时钟中断
							//其他位置置0，允许其他中断
		outb( 0xA1, 0xff);  //屏蔽从8259所有中断
}
